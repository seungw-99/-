'''
1. 파이참 단축키
열 선택하기: ctrl + ctrl = up/down # 빠르게 ctrl 두번, 방향키 하나 따다닥!
영역 다중 선택: alt + 마우스 클릭 # alt 누른 상태로 클릭
단어 다중선택: ctrl + shift + alt + J
단어 찾기: ctrl + F
라인 복사: ctrl + D
라인 이동: shift + alt + up/down (문법을 어기면서 이동) shift + ctrl + up/down (문법을 지키면서 이동)
라인 삭제: ctrl + Y
다음 줄로 바로 이동: shift + Enter

2. 윈도우 공통 단축키
뒤로 가기: ctrl + z
앞으로 가기: ctrl + shift + z
한줄 맨 앞 뒤 이동: home, end
맨 처음 텍스트 이동: ctrl + home
맨 뒤 텍스트 이동: ctrl + end
단어 단위 이동: ctrl + ◀/▶
단어 단위 삭제: ctrl + delete/backspace
'''
