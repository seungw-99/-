'''
#자료형(Data Type)
정수형(int): 음수, 0, 양수 데이터. Integer
실수형(float): 소수점을 포함한 숫자 데이터
문자열(str): 연속된 문자들의 나열. string
논리형(bool): 참, 거짓을 나타내는 논리형 데이터
'''
num1 = 10 #정수형
num2 = 10.0 #실수형 (소수점 포함)
msg = '10' # 문자열(따옴표 포함)
k = True # 논리형
print(num1, type(num1))
print(num2, type(num2))
print(msg, type(msg))
print(k, type(k))
print()

#같은 이름의 변수라도 대입하는 값이 바뀌면 데이터